/*
### 3.2 编程题 2：数位之和

- **试题名称**：数位之和
- **时间限制**：1.0 s
- **内存限制**：256 MB

#### 题目描述

给定一个正整数 $N$，将它的所有数位相加得到一个数 $S$；如果 $S$ 仍然大于等于 $10$，则继续把 $S$ 的各数位相加，直到结果是个一位数（即 $0$–$9$）为止。请输出这个最终的一位数。

例如：

- $N = 38$：$3 + 8 = 11$，$1 + 1 = 2$，输出 `2`。
- $N = 1234$：$1 + 2 + 3 + 4 = 10$，$1 + 0 = 1$，输出 `1`。

#### 输入格式

一行，包含一个正整数 $N$（$1 \le N \le 10^9$）。

#### 输出格式

一行，包含一个 $0$–$9$ 之间的整数，表示题目所述的"反复求数位和"后得到的一位数。

#### 样例

**输入样例 1**

```
38
```

**输出样例 1**

```
2
```

**输入样例 2**

```
1234
```

**输出样例 2**

```
1
```

**输入样例 3**

```
7
```

**输出样例 3**

```
7
```

#### 数据范围

$1 \le N \le 10^9$。

*/


#include <iostream>
using namespace std;
int main(){
    int N;
    int a=0;
    int b=0;
    cin>>N;
    do {
        b=0;
        while(N>0){
            a=N%10;
            b=b+a;
            N=N/10;
        }
        N=b;
    } while(b>9);
    cout<<b;
    return 0;
}